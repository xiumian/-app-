import { mkdir, writeFile } from 'node:fs/promises';
import { dirname } from 'node:path';

const args = new Set(process.argv.slice(2));
const productionMode = args.has('--production');
const targetIndex = process.argv.lastIndexOf('--target');
const target = targetIndex >= 0 ? process.argv[targetIndex + 1] : 'dist/runtime-config.js';

function envString(name, fallback = '') {
  return (process.env[name] || fallback).trim();
}

function envNumber(name, fallback) {
  const value = Number(process.env[name] || fallback);
  if (!Number.isFinite(value)) throw new Error(`${name} 必须是数字`);
  return value;
}

function envBoolean(name, fallback) {
  const value = envString(name, fallback ? 'true' : 'false').toLowerCase();
  if (value === 'true') return true;
  if (value === 'false') return false;
  throw new Error(`${name} 必须是 true 或 false`);
}

function isHttpsUrl(value) {
  try {
    return new URL(value).protocol === 'https:';
  } catch {
    return false;
  }
}

function isEmail(value) {
  const email = String(value || '').trim();
  return /^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$/i.test(email) && !email.includes('..');
}

function hasPlaceholder(value) {
  return /(example\.com|placeholder|todo|\u5f85\u5b9a|\u793a\u4f8b)/i.test(String(value || ''));
}

const config = {
  APP_RELEASE_CHANNEL: envString('PET_APP_RELEASE_CHANNEL', productionMode ? 'production' : 'local-production-ready'),
  API_BASE_URL: envString('PET_API_BASE_URL'),
  API_TIMEOUT_MS: envNumber('PET_API_TIMEOUT_MS', 8000),
  API_MOCK_FALLBACK: envBoolean('PET_API_MOCK_FALLBACK', !productionMode),
  MONITORING_ENDPOINT: envString('PET_MONITORING_ENDPOINT'),
  MONITORING_SAMPLE_RATE: envNumber('PET_MONITORING_SAMPLE_RATE', 1),
  OPERATOR_NAME: envString('PET_OPERATOR_NAME'),
  SUPPORT_CONTACT_LABEL: envString('PET_SUPPORT_CONTACT_LABEL', '客服与投诉入口'),
  SUPPORT_CONTACT_URL: envString('PET_SUPPORT_CONTACT_URL'),
  SUPPORT_EMAIL: envString('PET_SUPPORT_EMAIL')
};

if (productionMode) {
  const failures = [];
  if (config.APP_RELEASE_CHANNEL !== 'production') failures.push('PET_APP_RELEASE_CHANNEL 必须是 production');
  if (!isHttpsUrl(config.API_BASE_URL)) failures.push('PET_API_BASE_URL 必须是 HTTPS 地址');
  if (config.API_MOCK_FALLBACK !== false) failures.push('PET_API_MOCK_FALLBACK 必须是 false');
  if (!isHttpsUrl(config.MONITORING_ENDPOINT)) failures.push('PET_MONITORING_ENDPOINT 必须是 HTTPS 地址');
  if (!config.OPERATOR_NAME) failures.push('PET_OPERATOR_NAME 必须填写真实运营主体');
  if (!isHttpsUrl(config.SUPPORT_CONTACT_URL) && !isEmail(config.SUPPORT_EMAIL)) failures.push('PET_SUPPORT_CONTACT_URL 或 PET_SUPPORT_EMAIL 至少填写一个有效客服/投诉渠道');
  for (const key of ['PET_API_BASE_URL', 'PET_MONITORING_ENDPOINT', 'PET_OPERATOR_NAME', 'PET_SUPPORT_CONTACT_URL', 'PET_SUPPORT_EMAIL']) {
    const configKey = key.replace('PET_', '');
    if (hasPlaceholder(config[configKey])) failures.push(`${key} 不能使用 example.com、TODO、待定或示例占位内容`);
  }
  if (failures.length) throw new Error(`生产运行时配置不完整：${failures.join('；')}`);
}

if (config.API_TIMEOUT_MS < 1000 || config.API_TIMEOUT_MS > 30000) {
  throw new Error('PET_API_TIMEOUT_MS 必须在 1000 到 30000 之间');
}

if (config.MONITORING_SAMPLE_RATE < 0 || config.MONITORING_SAMPLE_RATE > 1) {
  throw new Error('PET_MONITORING_SAMPLE_RATE 必须在 0 到 1 之间');
}

const code = `// Generated by scripts/write-runtime-config.mjs. Do not put secrets here.
window.PET_COMPANION_CONFIG = ${JSON.stringify(config, null, 2)};
`;

await mkdir(dirname(target), { recursive: true });
await writeFile(target, code, 'utf8');

console.log(`Runtime config written: ${target}`);
