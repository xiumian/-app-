import { createReadStream } from 'node:fs';
import { stat } from 'node:fs/promises';
import { createServer } from 'node:http';
import { extname, join, normalize, resolve, sep } from 'node:path';
import { fileURLToPath } from 'node:url';
import { handleRequest } from './server/router.js';
import { installGracefulShutdown } from './server/lifecycle.js';

const rootDir = resolve(fileURLToPath(new URL('.', import.meta.url)));
const distDir = resolve(rootDir, 'dist');
const port = Number(process.env.PET_WEB_PORT || process.env.PORT || 5174);
const host = process.env.PET_WEB_HOST || '0.0.0.0';

const API_PREFIXES = [
  '/account',
  '/app-state',
  '/auth',
  '/health',
  '/media',
  '/monitoring',
  '/ready'
];

const MIME_TYPES = {
  '.css': 'text/css; charset=utf-8',
  '.html': 'text/html; charset=utf-8',
  '.js': 'text/javascript; charset=utf-8',
  '.json': 'application/json; charset=utf-8',
  '.png': 'image/png',
  '.svg': 'image/svg+xml',
  '.webmanifest': 'application/manifest+json; charset=utf-8'
};

function isApiRequest(pathname) {
  return API_PREFIXES.some(prefix => pathname === prefix || pathname.startsWith(`${prefix}/`));
}

function safeStaticPath(pathname) {
  let decoded = '/';
  try {
    decoded = decodeURIComponent(pathname.split('?')[0] || '/');
  } catch {
    return null;
  }
  const normalized = normalize(decoded).replace(/^(\.\.[/\\])+/, '');
  const relative = normalized === sep ? 'index.html' : normalized.replace(/^[/\\]+/, '');
  return resolve(distDir, relative);
}

function sendText(response, statusCode, body, headers = {}) {
  response.writeHead(statusCode, {
    'Content-Type': 'text/plain; charset=utf-8',
    'Cache-Control': 'no-store',
    'X-Content-Type-Options': 'nosniff',
    ...headers
  });
  response.end(body);
}

function sendRuntimeConfig(response) {
  const config = {
    APP_RELEASE_CHANNEL: process.env.PET_APP_RELEASE_CHANNEL || 'server-online',
    API_BASE_URL: process.env.PET_PUBLIC_API_BASE_URL || '.',
    API_TIMEOUT_MS: Number(process.env.PET_API_TIMEOUT_MS || 8000),
    API_MOCK_FALLBACK: String(process.env.PET_API_MOCK_FALLBACK || 'false').toLowerCase() === 'true',
    MONITORING_ENDPOINT: process.env.PET_MONITORING_ENDPOINT || '',
    MONITORING_SAMPLE_RATE: Number(process.env.PET_MONITORING_SAMPLE_RATE || 1)
  };
  response.writeHead(200, {
    'Content-Type': 'text/javascript; charset=utf-8',
    'Cache-Control': 'no-store',
    'X-Content-Type-Options': 'nosniff'
  });
  response.end(`// Generated by serve-production.mjs. Do not put secrets here.\nwindow.PET_COMPANION_CONFIG = ${JSON.stringify(config, null, 2)};\n`);
}

function sendStatic(response, filePath, statusCode = 200) {
  const type = MIME_TYPES[extname(filePath)] || 'application/octet-stream';
  response.writeHead(statusCode, {
    'Content-Type': type,
    'Cache-Control': filePath.endsWith('runtime-config.js') || filePath.endsWith('service-worker.js')
      ? 'no-store'
      : 'public, max-age=3600',
    'X-Content-Type-Options': 'nosniff',
    'Referrer-Policy': 'strict-origin-when-cross-origin',
    'Permissions-Policy': 'camera=(), microphone=(), geolocation=()'
  });
  const stream = createReadStream(filePath);
  stream.on('error', error => {
    console.error(error);
    if (!response.headersSent) sendText(response, 500, 'Static file unavailable');
    else response.destroy(error);
  });
  stream.pipe(response);
}

async function serveStatic(request, response, pathname) {
  if (pathname === '/runtime-config.js') {
    sendRuntimeConfig(response);
    return;
  }

  let filePath = safeStaticPath(pathname);
  if (!filePath) {
    sendText(response, 400, 'Bad Request');
    return;
  }
  if (!filePath.startsWith(`${distDir}${sep}`) && filePath !== distDir) {
    filePath = join(distDir, 'index.html');
  }

  try {
    const info = await stat(filePath);
    if (info.isFile()) {
      sendStatic(response, filePath);
      return;
    }
  } catch {}

  try {
    sendStatic(response, join(distDir, 'index.html'));
  } catch {
    sendText(response, 500, 'Static file unavailable');
  }
}

const server = createServer(async (request, response) => {
  try {
    const url = new URL(request.url || '/', `http://${request.headers.host || '127.0.0.1'}`);
    if (isApiRequest(url.pathname)) {
      await handleRequest(request, response);
      return;
    }
    await serveStatic(request, response, url.pathname);
  } catch (error) {
    console.error(error);
    if (!response.headersSent) sendText(response, 500, 'Internal Server Error');
    else response.destroy(error);
  }
});

server.requestTimeout = Number(process.env.PET_SERVER_REQUEST_TIMEOUT_MS || 30000);
server.headersTimeout = Number(process.env.PET_SERVER_HEADERS_TIMEOUT_MS || 15000);
server.keepAliveTimeout = Number(process.env.PET_SERVER_KEEP_ALIVE_TIMEOUT_MS || 5000);

server.listen(port, host, () => {
  console.log(`宠伴记已上线：http://${host}:${port}`);
});

installGracefulShutdown(server);
